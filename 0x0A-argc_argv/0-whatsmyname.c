#include <stdio.h>

/**
 * main - prints the name of the program
 *
 * @argc: stores the number of command line arguments
 * @argv: an array of pointers for all the command line arguments
 *
 * Return: return 0
 */
int main(int argc, char *argv[])
{
	argc = 0;
	printf("%s\n", argv[argc]);
	return (0);
}
